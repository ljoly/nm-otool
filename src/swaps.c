#include "ft_nm.h"

uint32_t	swp32(uint32_t val, t_bool swp)
{
	if (swp)
	{
		val = ((val << 8) & 0xFF00FF00) | ((val >> 8) & 0xFF00FF);
		val = (val << 16) | (val >> 16);
	}
	return (val);
}
